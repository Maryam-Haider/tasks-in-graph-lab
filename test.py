import turtle
m = turtle.Turtle()
m.color('red')
m.penup()
m.goto(-110, -25)
m.pendown()

m.circle(50)
m.color('red')
m.penup()
m.goto(0, -25)
m.pendown()
m.fillcolor('red')
m.circle(50)
m.color('red')
m.penup()
m.goto(110, -25)
m.pendown()
m.circle(50)
m.color('red')
m.penup()
m.goto(-55, -75)
m.pendown()
m.circle(50)
m.color('red')
m.penup()
m.goto(55, -76)
m.pendown()
m.circle(50)

turtle.done()